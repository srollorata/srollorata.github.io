function renderPortfolioItems() {
  const container = document.getElementById('portfolio-container');
  if (!container) return;

  container.innerHTML = portfolioItems.map(item => {
    const detailsUrl = item.detailsUrl || `portfolio-details.html?project=${item.id}`;
    return `
      <div class="col-lg-4 col-md-6 portfolio-item ${item.category}">
        <div class="portfolio-wrap">
          <img src="${item.image}" class="img-fluid" alt="${item.title}" />
          <div class="portfolio-info">
            <h4>${item.title}</h4>
            <p>${item.category.replace('filter-', '').replace(' filter-website filter-logo', '')}</p>
            <div class="portfolio-links">
              <a href="${item.lightboxImage}" data-gallery="portfolioGallery" class="portfolio-lightbox">
                <i class="bx bx-plus"></i>
              </a>
              <a href="${detailsUrl}" class="portfolio-details-lightbox" data-glightbox="type:iframe" title="Portfolio Details">
                <i class="bx bx-link"></i>
              </a>
            </div>
          </div>
        </div>
      </div>
    `;
  }).join('');
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', renderPortfolioItems);
} else {
  renderPortfolioItems();
}